package com.example.project.details;

import lombok.Getter;
import lombok.Setter;

@Getter
@Setter
public class Salary {

	 private int userId;
	 private String role;
	 private int salary;
	 private String body;
	 
	    private Salary() {
	    	super();
	    }

	@Override
	public String toString() {
		return "Salary [userId=" + userId + ", role=" + role + ", salary=" + salary + ", body=" + body + "]";
	}


	public Salary(int userId, String role, int salary, String body) {
		super();
		this.userId = userId;
		this.role = role;
		this.salary = salary;
		this.body = body;
	}
	 
}
